function soma (n1=0, n2=0){
    return n1 + n2
}
console.log(soma(4, 5))


//O =0 é um parâmetro opcional caso na tenha um valor digitado no n1 ou no n2, ou nos dois. Exemplo: console.log(soma(4)) Aqui só teve o valor do n1 que foi 4, o n2 não teve valor, então ele assumiu o valor 0 e a resposta foi 4. 